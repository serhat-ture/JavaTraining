package com.serhatcode.java101;

public class ComparisonOperators {
    public static void main(String[] args) {
        int aliAge =18;
        int mariamAge = 20;

        System.out.println(aliAge > mariamAge);
        System.out.println(aliAge < mariamAge);
        System.out.println(aliAge <= mariamAge);
        System.out.println(aliAge == mariamAge);
        System.out.println(aliAge != mariamAge);
    }
}
